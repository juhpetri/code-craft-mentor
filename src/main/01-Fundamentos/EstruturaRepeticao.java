package com.codecraftmentor.fundamentos;

public class EstruturaRepeticao {

    public static void main(String[] args) {
        // Exemplo com for
        for (int i = 1; i <= 5; i++) {
            System.out.println("Valor de i: " + i);
        }

        // Exemplo com while
        int j = 1;
        while (j <= 5) {
            System.out.println("Valor de j: " + j);
            j++;
        }
    }
}

